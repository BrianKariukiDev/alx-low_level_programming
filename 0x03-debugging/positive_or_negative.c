#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/**
 * main-Determine a negative,positive or zero number.
 * Return: 0(Success)
 */
int main(void)
{
	int i;

	if (i > 0)
	{
		printf("%d is positive\n", i);
	}
	else if (i == 0)
	{
		printf("%d is zero\n", i);
	}
	else
	{
		printf("%d is negative\n", i);
	}
	return (0);
}
